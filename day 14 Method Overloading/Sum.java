class Sum{
	int sum(int a,int b){
		int c=a+b;
		return c;
	}
	
	double sum(double a,double b){
		double c=a+b;
		return c;
	}
	
	int sum(int a,int b,int c){
		int d=a+b+c;
		return d;
	}
	
	double sum(double a,double b,double c){
		double d=a+b+c;
		return d;
	}
	
	int sum(int a,int b,int c,int d){
		int e=a+b+c+d;
		return e;
	}
	
	double sum(double a,double b,double c,double d){
		double e=a+b+c+d;
		return e;
	}
}